<p align="center">Dicas para os Iniciantes</p>

1- Leia as Regras, o Guia do Jogo e os Termos de Uso, assim você saberá como o jogo funciona e poderá evitar problemas no futuro.<br>
2- Sua energia é limitada, tome cuidado com o tipo de ação que vai ralizar.<br>
3- Fique atento ao ranking para saber como anda seu personagem em relação aos concorrentes.<br>
4- Você não poderá fazer algumas ações por causa da limitação de atributos.<br>
5- Concentre-se em melhorar sua popularidade. Esse é o objetivo do jogo.<br>
6- Ser popular com todos os grupos de alunos, ajudam sua popularidade geral subir de forma mais rápida.