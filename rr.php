<p align="center">Regras do Jogo</p>

<p>1- O jogo ser&aacute; disputado no formato de rounds.<br>
2- A cada seis semanas come&ccedil;ará um novo round. Dentro do jogo, são dez semanas, de Domingo a Terça, e de Quinta a Sábado.<br>
3- A administra&ccedil;&atilde;o do jogo ir&aacute; anunciar as datas do t&eacute;rmino e do in&iacute;cio de um novo round.<br>
4- Vencer&aacute; o round o jogador que tiver o maior numero de pontos no atributo 'Popularidade'.<br>
5- A cada fim de round, todos os jogadores ir&atilde;o sofrer o "reset" de seus atributos.<br>
6- O 'Mural' &eacute; um ambiente para interagir com outros jogadores. Mantenha o 'fair-play'.<br>
7- O 'Underground' &eacute; um ambiente criado para interagir com outros jogadores de forma ilustrativa.<br>
8- As a&ccedil;&otilde;es no 'Underground' n&atilde;o afetam os atributos do seu personagem.<br>
9- N&atilde;o tente trapacear.<br>
10- Se encontrar algum tipo de erro, avise aos administradores do jogo.<br>