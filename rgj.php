<p><a id="quests">QUESTS</a> - Quests são desafios especiais que estão disponíveis em alguns locais, por alguns personagens específicos. Cada uma das quests será realizada em uma semana específica. Você terá que descobrir sozinho quando cada quest está disponível. 
<p><a id="energia">ENERGIA</a> - Para cada ação que você realizar, você gastará energia. Quando sua energia estiver muito baixa, você pode ir descansar no Quarto. O tempo de descanso é de 6 horas, e após isso sua energia voltará a 100. Durante o tempo de descanso você não poderá ir a mais nenhum local, mas as outras áreas do site continuarão em funcionamento.
<p><a id="provas">SEMANA DE PROVAS</a> - Existe a semana de prova, que você verá no calendário. Se a sua nota for boa, você será recompensado. Se não...
<p><a id="popularidade">POPULARIDADES</a> - Existem dois tipos de popularidades. A popularidade geral é chamada nos atributos apenas de "Popularidade" e é esse o parametro que é usado para definir o ranking principal e definir quem é o vencedor de cada round do jogo. O outro tipo de popularidade é a popularidade específica. Essa é dividida em quatro: Popularidade entre os Nerds, Popularidade entre as Patricinhas, Popularidade entre os Esportistas e Popularidade entre os Marginais.
<p><a id="atributos">ATRIBUTOS</a> - São as características do seu personagem. Aparência, Força, Inteligência, Dinheiro e Energia. 
<br>Ações diferentes tem resultados com valores diferentes, mas uma mesma ação sempre tem o mesmo resultado.
<br>
<p><a id="mural">MURAL</a> - É a área do site destinada para a comunicação entre os usuários. Se o mundo precisa saber de alguma coisa, é aqui que estará escrito.
<br>
<p><a id="under">UNDERGROUND</a> - Área do site destinada ao confronto direto entre dois jogadores. Você precisa habilitar a opção de participar do Underground nas configurações. Após isso, você pode ir na opção "Brigar" e escolher um oponente. O jogo irá calcular os atributos dos dois e definir quem é o vencedor da briga. Você pode ver as brigas que já aconteceram e o ranking de brigas no Underground.<br>Vale na cara? Por favor!.
<p><a id="config">CONFIGURAÇÕES</a> - Nessa página você tem a opção de alterar suas informações de usuário. Também pode mudar a opção sobre participar ou não no Underground.
<p><a id="ranking">RANKING</a> - O ranking do jogo. Existem vários tipos de ranking, mas o vencedor do jogo é definido apenas pelo ranking de "Popularidade Geral". De todos os tipos de ranking, apenas o de "Popularidade Geral" e o de "Dinheiro" apresentam os valores. Os outros mostram apenas a sua colocação em relação aos outros personagens. 





 