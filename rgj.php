<p>O jogo é dividido entre ambientes, vamos descreve-los a seguir.
<p><a id="home">HOME</a>
<p> Essa página é responsável por mostrar o calendário oficial do jogo e os atributos do seu personagem. Do lado esquerdo há o menu com os locais por onde você pode ir:
<p><a id="diretoria"> DIRETORIA</a> - É a sala do diretor. Ações disponíveis: <br>
"Quero ver minhas notas": Com essa ação você poderá ver a sua média caso alguma prova já tenha sido realizada.<br>
"Conversar": Aqui você pode conversar com o Diretor.<br>
"Você tem algo especial para mim hoje?": Quest do diretor. Em alguma semana o Diretor irá pedir para você fazer alguma tarefa para ele.<br>
"Auxiliar": Auxilia o Diretor.<br>
"Vandalizar": Vandaliza a sala do Diretor.
